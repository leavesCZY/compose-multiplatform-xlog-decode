@Composable
fun AppTheme(
    theme: Theme,
    content: @Composable () -> Unit
) {
    val darkTheme = when (theme) {
        Theme.System -> {
            isSystemInDarkTheme()
        }

        Theme.Light -> {
            false
        }

        Theme.Dark -> {
            true
        }
    }
    val colors = if (darkTheme) {
        darkScheme
    } else {
        lightScheme
    }
    MaterialTheme(
        colorScheme = colors,
        typography = Typography,
        content = content
    )
}